class RiskAssessor:
    """Convert posture deviations into injury risk level"""

    def assess_risk(self, deviations: List[PostureDeviation]) -> RiskLevel:
        """
        Calculate overall injury risk based on deviation severity
        """
        if not deviations:
            return RiskLevel.LOW

        # Count deviations by severity
        severe_count = sum(1 for d in deviations if d.severity == "severe")
        moderate_count = sum(1 for d in deviations if d.severity == "moderate")
        minor_count = sum(1 for d in deviations if d.severity == "minor")

        # Risk decision logic
        if severe_count >= 2:
            return RiskLevel.CRITICAL
        elif severe_count >= 1:
            return RiskLevel.HIGH
        elif moderate_count >= 2:
            return RiskLevel.HIGH
        elif moderate_count >= 1:
            return RiskLevel.MEDIUM
