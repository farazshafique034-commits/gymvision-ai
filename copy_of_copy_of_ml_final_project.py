class PostureEvaluator:
    """Detect biomechanical deviations using rule-based logic"""

    def evaluate_posture(
        self,
        metrics: BiomechanicalMetrics,
        movement: MovementCategory
    ) -> List[PostureDeviation]:
        """
        Apply movement-specific safety rules
        Returns list of detected deviations
        """
        deviations = []

        # Apply movement-specific checks
        if movement == MovementCategory.HIP_HINGE:
            deviations.extend(self._check_hip_hinge_form(metrics))
        elif movement == MovementCategory.LOWER_BODY_COMPOUND:
            deviations.extend(self._check_squat_form(metrics))
        elif movement == MovementCategory.UPPER_PUSH:
            deviations.extend(self._check_push_form(metrics))
        elif movement == MovementCategory.OVERHEAD_MOVEMENT:
            deviations.extend(self._check_overhead_form(metrics))

        # Universal checks (apply to all movements)
        deviations.extend(self._check_universal_issues(metrics))

        return deviations

    def _check_hip_hinge_form(self, m: BiomechanicalMetrics) -> List[PostureDeviation]:
        """Check hip hinge pattern safety"""
        issues = []

        # Excessive spinal flexion
        if m.torso_lean and m.torso_lean > 50:
            issues.append(PostureDeviation(
                issue="Excessive forward lean",
                severity="severe",
                affected_joint="Spine",
                description="Torso angle exceeds safe range for hip hinge pattern"
            ))

        # Premature knee flexion
        if m.knee_angle_left and m.knee_angle_left < 140:
            issues.append(PostureDeviation(
                issue="Excessive knee bend",
                severity="moderate",
                affected_joint="Knee",
                description="Knees should remain relatively extended in hip hinge"
            ))

        return issues

    def _check_squat_form(self, m: BiomechanicalMetrics) -> List[PostureDeviation]:
        """Check squat pattern safety"""
        issues = []

        # Knee valgus check (asymmetric knee angles)
        if m.knee_angle_left and m.knee_angle_right:
            knee_diff = abs(m.knee_angle_left - m.knee_angle_right)
            if knee_diff > 15:
                issues.append(PostureDeviation(
                    issue="Knee asymmetry detected",
                    severity="moderate",
                    affected_joint="Knees",
                    description="Left and right knee angles differ significantly"
                ))

        # Excessive forward lean
        if m.torso_lean and m.torso_lean > 35:
            issues.append(PostureDeviation(
                issue="Excessive forward torso lean",
                severity="moderate",
                affected_joint="Spine",
                description="Torso should remain more upright in squat pattern"
            ))

        # Insufficient depth (if knee angle > 120 in squat position)
        if m.knee_angle_left and m.knee_angle_left > 120:
            issues.append(PostureDeviation(
                issue="Limited range of motion",
                severity="minor",
                affected_joint="Hip/Knee",
                description="Movement depth may be suboptimal"
            ))

        return issues

    def _check_push_form(self, m: BiomechanicalMetrics) -> List[PostureDeviation]:
        """Check pushing movement safety"""
        issues = []

        # Elbow flare (overly extended elbows laterally)
        # Note: This is simplified - full analysis would need shoulder landmarks
        if m.elbow_angle_left and m.elbow_angle_left < 45:
            issues.append(PostureDeviation(
                issue="Excessive elbow flexion",
                severity="minor",
                affected_joint="Elbow",
                description="Elbow position may stress shoulder joint"
            ))

        return issues

    def _check_overhead_form(self, m: BiomechanicalMetrics) -> List[PostureDeviation]:
        """Check overhead movement safety"""
        issues = []

        # Excessive back arch during overhead press
        if m.torso_lean and m.torso_lean > 20:
            issues.append(PostureDeviation(
                issue="Excessive spinal extension",
                severity="severe",
                affected_joint="Spine",
                description="Lower back arching compensates for limited shoulder mobility"
            ))

        return issues

    def _check_universal_issues(self, m: BiomechanicalMetrics) -> List[PostureDeviation]:
        """Check issues common to all movements"""
        issues = []

        # Lateral asymmetry in hips
        if m.hip_angle_left and m.hip_angle_right:
            hip_diff = abs(m.hip_angle_left - m.hip_angle_right)
            if hip_diff > 20:
                issues.append(PostureDeviation(
                    issue="Lateral hip asymmetry",
                    severity="moderate",
                    affected_joint="Hips",
                    description="Uneven loading between left and right side"
                ))

        return issues

