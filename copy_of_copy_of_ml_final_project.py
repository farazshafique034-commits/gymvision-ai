class MovementClassifier:
    """Identify general movement categories from biomechanics"""

    def classify_movement(self, metrics: BiomechanicalMetrics) -> MovementCategory:
        """
        Classify movement pattern based on joint configuration
        (Conservative, priority-based approach to avoid overlap)
        """

        # Check for overhead movement (highest priority)
        if self._is_overhead_movement(metrics):
            return MovementCategory.OVERHEAD_MOVEMENT

        # Check for hip hinge pattern
        if self._is_hip_hinge(metrics):
            return MovementCategory.HIP_HINGE

        # Check for lower body compound
        if self._is_lower_body_compound(metrics):
            return MovementCategory.LOWER_BODY_COMPOUND

        # Check for upper body push
        if self._is_upper_push(metrics):
            return MovementCategory.UPPER_PUSH

        # Check for upper body pull
        if self._is_upper_pull(metrics):
            return MovementCategory.UPPER_PULL

        # Check for core stability
        if self._is_core_stability(metrics):
            return MovementCategory.CORE_STABILITY

        return MovementCategory.UNKNOWN

    def _is_hip_hinge(self, m: BiomechanicalMetrics) -> bool:
        """Detect hip hinge pattern (deadlift, RDL, good morning)"""
        # Forward torso lean + extended knees + hip flexion
        return (
            m.torso_lean is not None and m.torso_lean > 25 and
            m.knee_angle_left is not None and m.knee_angle_left > 150 and
            m.hip_angle_left is not None and m.hip_angle_left < 100
        )

    def _is_lower_body_compound(self, m: BiomechanicalMetrics) -> bool:
        """Detect squat-like movements"""
        # Deep knee flexion + hip flexion + relatively upright torso
        return (
            m.knee_angle_left is not None and m.knee_angle_left < 120 and
            m.hip_angle_left is not None and m.hip_angle_left < 120 and
            m.torso_lean is not None and m.torso_lean < 25
        )

    def _is_upper_push(self, m: BiomechanicalMetrics) -> bool:
        """Detect push movements (bench press, pushup, shoulder press)"""
        # Elbow flexion in push position
        return (
            m.elbow_angle_left is not None and
            40 < m.elbow_angle_left < 150
        )

    def _is_upper_pull(self, m: BiomechanicalMetrics) -> bool:
        """Detect pull movements (rows, pullups)"""
        # Elbow flexion with potential shoulder retraction
        return (
            m.elbow_angle_left is not None and
            m.elbow_angle_left < 90 and
            m.shoulder_elevation_left is not None
        )

    def _is_overhead_movement(self, m: BiomechanicalMetrics) -> bool:
        """Detect overhead movements (shoulder press, snatch)"""
        # Arms elevated above shoulders
        return (
            m.shoulder_elevation_left is not None and
            m.shoulder_elevation_left < -50
        )

    def _is_core_stability(self, m: BiomechanicalMetrics) -> bool:
        """Detect core-focused movements (plank, holds)"""
        # Horizontal body position with extended limbs
        return (
            m.torso_lean is not None and m.torso_lean < 10 and
            m.knee_angle_left is not None and m.knee_angle_left > 160
        )

