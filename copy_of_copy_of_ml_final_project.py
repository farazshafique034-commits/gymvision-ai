class GymFormAnalyzer:
    """Main analysis orchestrator"""

    def __init__(self):
        self.pose_estimator = PoseEstimator()
        self.biomech_analyzer = BiomechanicsAnalyzer()
        self.movement_classifier = MovementClassifier()
        self.posture_evaluator = PostureEvaluator()
        self.risk_assessor = RiskAssessor()
        self.feedback_generator = FeedbackGenerator()

    def analyze_image(self, image_path: str) -> AnalysisResult:
        """
        Complete end-to-end analysis pipeline
        """
        # Load image
        image = cv2.imread(image_path)
        if image is None:
            raise ValueError(f"Could not load image: {image_path}")

        img_name = Path(image_path).name

        # Step 1: Detect pose landmarks
        landmarks = self.pose_estimator.detect_landmarks(image)

        if landmarks is None:
            # No pose detected
            return AnalysisResult(
                image_name=img_name,
                movement_category=MovementCategory.UNKNOWN,
                biomechanics=BiomechanicalMetrics(),
                deviations=[],
                risk_level=RiskLevel.LOW,
                feedback="⚠ No person detected in image. Ensure the subject is clearly visible.",
                annotated_image=image,
                pose_detected=False
            )

        # Step 2: Draw skeleton
        annotated_image = self.pose_estimator.draw_skeleton(image, landmarks)

        # Step 3: Extract biomechanical metrics
        metrics = self.biomech_analyzer.extract_metrics(landmarks, image.shape)

        # Step 4: Classify movement pattern
        movement = self.movement_classifier.classify_movement(metrics)

        # Step 5: Evaluate posture deviations
        deviations = self.posture_evaluator.evaluate_posture(metrics, movement)

        # Step 6: Assess injury risk
        risk = self.risk_assessor.assess_risk(deviations)

        # Step 7: Generate coaching feedback
        feedback = self.feedback_generator.generate_feedback(movement, deviations, risk)

        # Add risk indicator to image
        annotated_image = self._add_risk_overlay(annotated_image, risk)

        return AnalysisResult(
            image_name=img_name,
            movement_category=movement,
            biomechanics=metrics,
            deviations=deviations,
            risk_level=risk,
            feedback=feedback,
            annotated_image=annotated_image,
            pose_detected=True
        )

    def _add_risk_overlay(self, image: np.ndarray, risk: RiskLevel) -> np.ndarray:
        """Add risk level indicator to image"""
        h, w = image.shape[:2]
        overlay = image.copy()

        # Risk badge
        color = RiskAssessor().get_risk_color(risk)
        cv2.rectangle(overlay, (10, 10), (250, 60), color, -1)
        cv2.rectangle(overlay, (10, 10), (250, 60), (255, 255, 255), 2)

        cv2.putText(
            overlay,
            f"RISK: {risk.value.upper()}",
            (20, 45),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.8,
            (0, 0, 0),
            2,
            cv2.LINE_AA
        )
        return overlay

