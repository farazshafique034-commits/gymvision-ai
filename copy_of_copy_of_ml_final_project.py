class PoseEstimator:
    """MediaPipe-based pose landmark detection"""

    # MediaPipe landmark indices
    LANDMARKS = {
        'nose': 0,
        'left_eye_inner': 1, 'left_eye': 2, 'left_eye_outer': 3,
        'right_eye_inner': 4, 'right_eye': 5, 'right_eye_outer': 6,
        'left_ear': 7, 'right_ear': 8,
        'mouth_left': 9, 'mouth_right': 10,
        'left_shoulder': 11, 'right_shoulder': 12,
        'left_elbow': 13, 'right_elbow': 14,
        'left_wrist': 15, 'right_wrist': 16,
        'left_pinky': 17, 'right_pinky': 18,
        'left_index': 19, 'right_index': 20,
        'left_thumb': 21, 'right_thumb': 22,
        'left_hip': 23, 'right_hip': 24,
        'left_knee': 25, 'right_knee': 26,
        'left_ankle': 27, 'right_ankle': 28,
        'left_heel': 29, 'right_heel': 30,
        'left_foot_index': 31, 'right_foot_index': 32
    }

    def __init__(self):
        self.mp_pose = mp.solutions.pose
        self.mp_draw = mp.solutions.drawing_utils
        self.pose = self.mp_pose.Pose(
            static_image_mode=True,
            model_complexity=2,
            min_detection_confidence=0.5,
            min_tracking_confidence=0.5
        )

    def detect_landmarks(self, image: np.ndarray) -> Optional[any]:
        """Extract 33 body landmarks from image"""
        image_rgb = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
        results = self.pose.process(image_rgb)
        return results.pose_landmarks if results.pose_landmarks else None

    def draw_skeleton(self, image: np.ndarray, landmarks: any) -> np.ndarray:
        """Visualize pose skeleton on image"""
        annotated = image.copy()
        self.mp_draw.draw_landmarks(
            annotated,
            landmarks,
            self.mp_pose.POSE_CONNECTIONS,
            self.mp_draw.DrawingSpec(color=(0, 255, 0), thickness=2, circle_radius=3),
            self.mp_draw.DrawingSpec(color=(0, 0, 255), thickness=2)
        )
        return annotated

    def get_landmark_coords(self, landmarks: any, idx: int, img_shape: Tuple) -> Optional[Tuple[float, float]]:
        """Convert normalized landmarks to pixel coordinates"""
        if not landmarks or idx >= len(landmarks.landmark):
            return None
        lm = landmarks.landmark[idx]
        if lm.visibility < 0.5:  # Confidence threshold
            return None
        h, w = img_shape[:2]
        return (int(lm.x * w), int(lm.y * h))

