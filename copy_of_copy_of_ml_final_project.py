def augment_data(X, y, n_aug=12, noise_std=4.0, seed=42):
 """
 For each real sample, create n_aug synthetic neighbours by adding
 small Gaussian noise. This increases dataset size for stable CV.
 """
 # Debugging: Check for single class input that would cause SVM error
 if len(set(y)) < 2:
  warnings.warn(
      "Input 'y' to augment_data contains only one unique class. "
      "This will lead to an error if used for multi-class classification training (e.g., SVM)."
      "Augmentation proceeds, but the resulting dataset will still be single-class."
  )

 rng = np.random.RandomState(seed)
 X_aug, y_aug = [X.copy()], [list(y)]
 for _ in range(n_aug):
  noise = rng.normal(0, noise_std, size=X.shape)
  X_aug.append(X + noise)
  y_aug.append(list(y))
 return np.vstack(X_aug), sum(y_aug, [])
